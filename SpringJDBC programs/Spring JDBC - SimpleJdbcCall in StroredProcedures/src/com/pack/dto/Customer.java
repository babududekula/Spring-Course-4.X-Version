package com.pack.dto;

public class Customer 
{
	private int cid;
	private String cname;
	private int cage;
	private float csal;
	public int getCid() {
		return cid;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public int getCage() {
		return cage;
	}
	public void setCage(int cage) {
		this.cage = cage;
	}
	public float getCsal() {
		return csal;
	}
	public void setCsal(float csal) {
		this.csal = csal;
	}
	
	
}
